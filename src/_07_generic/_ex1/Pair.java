package _07_generic._ex1;

public class Pair<K,V> {
    private K key;
    private V value;

    public Pair(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }
    public String toString(){
        return "Key : "+key+", Value : "+value ;
    }

}
